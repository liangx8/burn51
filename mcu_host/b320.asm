; function pi_read256 {{{
; use variable "hi_addr" target address high byte,
pi_read256:
	mov		a,#0xb4
	acall	c2_addr_write
	mov		a,#6				; block read command
	acall	c2_data_write
	acall	pi_inbusy
	acall	result_check
	mov		a,hi_addr				; address high
	acall	c2_data_write
	acall	pi_inbusy

	clr		a					; address low
	acall	c2_data_write
	acall	pi_inbusy

	clr		a					; length of data
	acall	c2_data_write
	acall	pi_inbusy
	acall	result_check

	mov		r1,#0
2$:
	acall	c2_data_read
	movx	@r1,a
	inc		r1
	mov		a,r1
	jnz		2$
	ret
; }}}
; function pi_write256 {{{
; hi_addr target address high
pi_write256:
	mov		a,#0xb4
	acall	c2_addr_write
	mov		a,#7				; block write command
	acall	c2_data_write
	acall	pi_inbusy
	acall	result_check
	mov		a,hi_addr			; address high byte
	acall	c2_data_write
	acall	pi_inbusy
	clr		a					; address low byte
	acall	c2_data_write
	acall	pi_inbusy
	clr		a					; data length 256
	acall	c2_data_write
	acall	pi_inbusy
	mov		r1,#0
1$:
	movx	a,@r1
	acall	c2_data_write
	acall	pi_inbusy
	inc		r1
	mov		a,r1
	jnz		1$
	acall	pi_inbusy
	sjmp	result_check

; }}}
; function pi_erase_device {{{
pi_erase_device:
	mov		a,#0xb4
	acall	c2_addr_write
	mov		a,#3				; erase device command
	acall	c2_data_write
	acall	pi_inbusy
	acall	result_check
	mov		a,#0xde
	acall	c2_data_write
	acall	pi_inbusy
	mov		a,#0xad
	acall	c2_data_write
	acall	pi_inbusy
	mov		a,#0xa5
	acall	c2_data_write
	acall	pi_inbusy
	sjmp	result_check
; }}}
result_check:
	acall	pi_outready
	acall	c2_data_read
	cjne	a,#0x0d,1$	; error
	ret
1$:
	clr		GREEN_LED
	setb	RED_LED
	; 这里有些返回是从函数中直接跳回来。因此。需要重新复位一下堆栈
	mov		sp,#0xcf
	mov		psw,#0
	ajmp	burn_ready
; }}}
